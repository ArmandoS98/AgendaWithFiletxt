    private void jButton2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MousePressed
        // TODO add your handling code here:
        ArrayList<Datos> empleados = new ArrayList<>();
        Datos datos = new Datos();

        if (noHayVacios()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios!");

        } else {
            datos.setPrimerNombre(txtPrimerNombre.getText());
            datos.setSegundoNombre(txtSegundoNombre.getText());
            datos.setPrimerApellido(txtPreimerApellido.getText());
            datos.setSegundoApellido(txtSegundoApellido.getText());
            datos.setNombreEmpresa(txtNombreEmpresa.getText());
            datos.setFechaIncio(cbxDia.getSelectedItem() + "/" + (cbxMes.getSelectedIndex() + 1) + "/" + cbxAnio.getSelectedItem());
            empleados.add(datos);

            if (existe()) {
                OperacionArchivo.aniadirArchivo(empleados);
            } else {
                OperacionArchivo.crearArchivo(empleados);
            }

            limpiar();
            cargarTabla1();
        }
    }//GEN-LAST:event_jButton2MousePressed

