    private boolean existe() {
        File file = new File(NOMBRE_ARCHIVO);
        return file.exists();
    }

    private void limpiar() {
        txtPrimerNombre.setText("");
        txtSegundoNombre.setText("");
        txtPreimerApellido.setText("");
        txtSegundoApellido.setText("");
        txtNombreEmpresa.setText("");
        cbxDia.setSelectedIndex(0);
        cbxMes.setSelectedIndex(0);
        cbxAnio.setSelectedIndex(0);
    }

    private boolean noHayVacios() {
        if (txtPrimerNombre.getText().isEmpty()
                || txtSegundoNombre.getText().isEmpty()
                || txtPreimerApellido.getText().isEmpty()
                || txtSegundoApellido.getText().isEmpty()
                || txtNombreEmpresa.getText().isEmpty()) {
            return true;
        } else {
            return false;
        }
    }

    private void cargarTabla() {
        ArrayList<Datos> listaLeida = new ArrayList<>();
        listaLeida = OperacionArchivo.leerArchivo();

        for (Datos datos1 : listaLeida) {
            ((DefaultTableModel) tbEmpleados.getModel()).addRow(new Object[]{
                datos1.getPrimerNombre(),
                datos1.getSegundoNombre(),
                datos1.getPrimerApellido(),
                datos1.getSegundoApellido(),
                datos1.getNombreEmpresa(),
                datos1.getFechaIncio()
            });

        }
    }

    private void cargarTabla1() {
        ArrayList<Datos> listaLeida = new ArrayList<>();
        listaLeida = OperacionArchivo.leerArchivo();
        int pos = listaLeida.size();

        for (int i = 0; i < pos; i++) {
            if (i == (pos - 1)) {
                ((DefaultTableModel) tbEmpleados.getModel()).addRow(new Object[]{
                    listaLeida.get(i).getPrimerNombre(),
                    listaLeida.get(i).getSegundoNombre(),
                    listaLeida.get(i).getPrimerApellido(),
                    listaLeida.get(i).getSegundoApellido(),
                    listaLeida.get(i).getNombreEmpresa(),
                    listaLeida.get(i).getFechaIncio()
                });
            }
        }

    }
